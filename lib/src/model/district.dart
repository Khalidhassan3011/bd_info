  final String name;
  final String nameBangla;
  final String division;
  final int totalUpazilas;
  final int totalVillage;
  final double area;

  const District({
    required this.name,
    required this.nameBangla,
    required this.division,
    required this.totalUpazilas,
    required this.totalVillage,
    required this.area,
  });
}
